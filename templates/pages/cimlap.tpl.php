        <p>A Formula–1 (rövidítve: F1, angolul: Formula One) a legrangosabb nemzetközi autóverseny-sorozat, melynek szabályait a Nemzetközi Automobil Szövetség határozza meg. A Formula–1-es világbajnokságban nyitott karosszériájú, együléses versenyautók vehetnek részt. A versenyeket szilárd burkolatú, erre a célra kialakított zárt, vagy városi pályákon rendezik. Az eredmények alapján évente két világbajnokot avatnak, egy egyénit és egy konstruktőrit (csapat-világbajnokság). A versenyeket több százmillióan nézik televízión, a világ több mint kétszáz országában.</p>
        
